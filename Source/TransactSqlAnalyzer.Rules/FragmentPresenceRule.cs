        public override string RuleTypeDescription => "Detects the presence of TSQL language elements.";

        public FragmentPresenceRuleConfig SpecificConfiguration { get; private set; } = new FragmentPresenceRuleConfig();

        public void SetSpecificConfiguration(ISpecificConfiguration specificConfiguration)
        {
            Check.OfType<FragmentPresenceRuleConfig>(specificConfiguration, nameof(specificConfiguration));
            SpecificConfiguration = (FragmentPresenceRuleConfig)specificConfiguration;
        }

        public override void Visit(TSqlFragment node)
        {
            base.Visit(node);

            var fragmentType = node.GetType();
            var isFragmentPresent = SpecificConfiguration.AffectedFragmentTypes.Any(aX => aX.IsAssignableFrom(fragmentType));
            if (isFragmentPresent)
            {
                AddFinding(node, $"{SpecificConfiguration.ViolationMessage} ({fragmentType.Name})");
            }
        }
